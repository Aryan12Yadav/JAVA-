// Typecasting

public class P1 {
   
    public static void main(String []args){
    
    int a= 45;
    double b;
    b = a;
    System.out.println(b);



    double num = 234;
    int num2;
    num2 = (int)num;  /* this is explicity typecasting */
    System.out.println(num2);



    int n1 = 12;
    int n2 = 5;
    float result = n1/n2;
    System.out.println(result);

    
    }
}